<script>
  const questItems = document.querySelectorAll(".questions__item");

questItems.forEach((element, index) => {
  element.addEventListener("click", () => {
    const checkItemOpen = document.querySelector(".content-open");

    toggleItem(element);

    if (checkItemOpen && checkItemOpen !== element) {
      toggleItem(checkItemOpen);
    }
  });
});

const toggleItem = (element) => {
  const questContent = element.querySelector(".questions__content");

  if (element.classList.contains("content-open")) {
    questContent.removeAttribute("style");
    element.classList.remove("content-open");
  } else {
    questContent.style.height = questContent.scrollHeight + "px";
    element.classList.add("content-open");
  }
};

</script>
